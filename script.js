
let color = prompt('Ընտրեք լուսացույցի գույնը (Հայ,Eng,Рус)');
if (color == 'կանաչ') {
    alert('Անցեք')
}else if (color=='դեղին'){
    alert('Սպասեք')
}else if (color=='կարմիր') {
    alert('Կանգնեք')
}else if (color=='green') {
    alert('go')
}else if (color=='yellow') {
    alert('wait')
}else if (color=='red') {
    alert('stop')
}else if (color=='зеленый') {
    alert('проходите')
}else if (color=='желтый') {
    alert('ждите')
}else if (color=='красный') {
    alert('стойте')
}else {
    alert('Սխալ մուտքագրում')
}